/*
 * isZero - returns 1 if x == 0, and 0 otherwise 
 *   Examples: isZero(5) = 0, isZero(0) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 2
 *   Rating: 1
 */
int isZero(int x) {
  // !0 wil return 0x01 and others will return 0x00 as requested
  return !x;
}
/* 
 * implication - return x -> y in propositional logic - 0 for false, 1
 * for true
 *   Example: implication(1,1) = 1
 *            implication(1,0) = 0
 *   Legal ops: ! ~ ^ |
 *   Max ops: 5
 *   Rating: 2
 */
int implication(int x, int y) {
  // mathematical equivalent of x -> y == (x' or y)
  return (!x) | y;
}
/* 
 * TwoDigitNumberInBaseFour - return integer equivalent of (xy)_4 two digit number in base 4
 *   Example: twoDigitNumberInBaseFour(2, 3) = 11
 *   Legal ops: >> << +
 *   Max ops: 4
 *   Rating: 2
 */
unsigned twoDigitNumberInBaseFour(unsigned x, unsigned y) {
  // mathematicaly equivalent of 4^x + y. 4^x == x << 2
  return (x << 2) + y;
}
/*
 * multThreeEighths - multiplies by 3/8 rounding toward 0.
 *   Should exactly duplicate effect of C expression (x*3/8),
 *   including overflow behavior.
 *   Examples: multThreeEighths(77) = 28
 *             multThreeEighths(-22) = -8
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 12
 *   Rating: 3
 */
int multThreeEighths(int x) {
  // n >> 3 part multiplies by 3 and the rest divides by 8
  return (((((x << 1) + x) >> 31 ) & 7) + (x << 1) + x) >> 3; 
}
/* 
 * bang - Compute !x without using !
 *   Examples: bang(3) = 0, bang(0) = 1
 *   Legal ops: ~ & ^ | + << >>
 *   Max ops: 12
 *   Rating: 4 
 */
int bang(int x) {
  // x and -x are positive for only x=0 then >> 31 + 1 to return 1 for only 0
  return ((x | (~x +1)) >> 31) + 1;
}
/* 
 * TMax - return maximum two's complement integer 
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 4
 *   Rating: 1
 */
int tmax(void) {
  // 0x7fffffff = ~1<<31
  return ~(1 << 31);
}
/* 
 * isOppositeSigns - if x and y has different signs then return 1, else return 0 
 *   Example: isOppositeSigns(4,-5) = 1.
 *            isOppositeSigns(3,2) = 0
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 3
 */
int isOppositeSigns(int x, int y) {
  // xor(sign bit of x, sign bit of y) so that it gives 1 for opposite signs
  return !(x >> 31) ^ !(y >> 31);
}
/* 
 * conditional - same as x ? y : z 
 *   Example: conditional(2,4,5) = 4
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 16
 *   Rating: 3
 */
int conditional(int x, int y, int z) {
  // !x + 0x00 as mask, x!=0 mask out z otherwise mask out y
  return ((~(!x + ~0x00) & z ) | ((!x + ~0x00) & y));
}
/* 
 * float_abs - Return bit-level equivalent of absolute value of f for
 *   floating point argument f.
 *   Both the argument and result are passed as unsigned int's, but
 *   they are to be interpreted as the bit-level representations of
 *   single-precision floating point values.
 *   When argument is NaN, return argument..
 *   Legal ops: Any integer/unsigned operations incl. ||, &&. also if, while
 *   Max ops: 10
 *   Rating: 2
 */
unsigned float_abs(unsigned uf) {
  // result = ans(uf) but checks if it is NaN
  unsigned result = uf & 0x7FFFFFFF;
  if ((result >= 0x7F800001)){
	return uf;
  }
  return result;
}
/* 
 * float_f2i - Return bit-level equivalent of expression (int) f
 *   for floating point argument f.
 *   Argument is passed as unsigned int, but
 *   it is to be interpreted as the bit-level representation of a
 *   single-precision floating point value.
 *   Anything out of range (including NaN and infinity) should return
 *   0x80000000u.
 *   Legal ops: Any integer/unsigned operations incl. ||, &&. also if, while
 *   Max ops: 30
 *   Rating: 4
 */
int float_f2i(unsigned uf) {
  unsigned exp = (uf >> 23) & 0xFF;
  unsigned frac = (uf & 0x7FFFFF);
  
  // out of range check exp part
  if (exp == 0xFF)
  {
    return 0x80000000u;
  }
  
  if (exp < 0x7F)
  {
    return 0x0;
  }

  exp -= 0x7F;

  // overeflow check exp part
  if (exp >= 31)
  {
    return 0x80000000u;
  }

  // integer result
  if (exp > 22)
  {
    frac = frac << (exp - 23);
  }
  else
  {
    frac = frac >> (23 - exp);
  }
  
  frac += 1 << exp;
  
  // absolute value
  if (uf >> 31)
  { 
    frac = -frac;
  }

  return frac;
}
